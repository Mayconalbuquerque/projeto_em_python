"""
Exercícios
Crie funções que duplicam, triplicam e quadriplicam
o número recebido como parâmetro
"""


def duplicate(number):
    return number * 2

def triplicate(number):
    return number * 3

def quadruplicate(number):
    return number * 4


print('O dobro no número selecionado é', duplicate(4))
print('O triplo no número selecionado é', triplicate(4))
print('O quadruplo no número selecionado é', quadruplicate(4))

